package main;
import java.lang.Math.*;

public class Case
{
    private int x;
    private int y;
    private int value;
    
    public Case(){
        this.x = x;
        this.y = y;
        this.value = value;
    }
    
    // getters
    public int getX(){
        return this.x;
    }
     public int getY(){
        return this.y;
    }
      public int getValue(){
        return this.value;
    }
      
    //setters
      
    public void setX(int x){
        this.x = x;
    }
    
    public void setY(int y){
        this.y = y;
    }
    
    public void setValue(int value){
        this.value = value;
    }
      
      
}
